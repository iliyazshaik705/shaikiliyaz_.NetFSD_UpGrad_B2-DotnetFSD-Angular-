
function studentGrade(number){
    let a=number;
    if (a>=75){
        console.log("Grade A");
        
    }
    else if(a>=60){
        console.log("Grade B");
        
    }
    else if(a>=40){
        console.log("Grade c");
        
    }
    else{
        console.log("Fail")
    }
}


studentGrade(41);